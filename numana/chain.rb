<HEAD1>Multivariate chain rule</HEAD1>
Recall the univariate chain rule: If <M>f:\rr\to\rr</M>
and <M>g:\rr\to\rr</M> are two differentiable functions,
then <M>[[d][dx]]f(g(x)) = f'(g(x)) g'(x).</M>
<P/>
If <M>f:\rr^2\to\rr</M> and <M>g,h:\rr\to\rr</M>, then we have
<D>
[[d][dx]] f(g(x), h(x)) = f_1(g(x), h(x)) g'(x) + f_2(g(x), h(x)) h'(x).
</D>
We shall not prove it here. But let's see a demonstration.
<P/>
Suppose that <M>f(x,y) = (x+y)^2,</M> <M>g(x) = e^x</M>
and <M>h(x) = \sin x.</M> Then we see that
<D>
f(g(x), h(x)) = (e^x + \sin x)^2.
</D>
Differentiating this using the univariate chain rule we get 
<D>
[[d][dx]] f(g(x), h(x)) = 2(e^x + \sin x) (e^x + \cos x).
</D>
To use the multivariate chain rule, note that <M>f_1(x,y) =
f_2(x,y) = 2(x+y),</M> and <M>g'(x) = e^x</M> and <M>h'(x) = \cos
x.</M> So we have
<D>
[[d][dx]] f(g(x), h(x)) = 2(e^x + \sin x) e^x + 2(e^x + \sin x)  \cos x,
</D>
which is the same as what we got earlier.
